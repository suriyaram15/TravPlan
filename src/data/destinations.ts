// Get trending destinations
export const getTrending = () => destinations.filter(dest => dest.trending);

// Get destinations with offers
export const getOffers = () => destinations.filter(dest => dest.offer);

// Get destinations by region
export const getByRegion = (region: string) => 
  destinations.filter(dest => dest.region === region);

// Get destinations by state
export const getByState = (state: string) => 
  destinations.filter(dest => dest.state === state);

// Get destination by ID
export const getById = (id: string) => 
  destinations.find(dest => dest.id === id);

// Search destinations
export const searchDestinations = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return destinations.filter(dest => 
    dest.name.toLowerCase().includes(lowercaseQuery) || 
    dest.state.toLowerCase().includes(lowercaseQuery) || 
    dest.description.toLowerCase().includes(lowercaseQuery)
  );
};
